package Section01;

public class _03_DataTypes {
    /*
    Primitive (Ilkel) Data Types:

    * * Integer * *
    byte -> 8 bit       / From +127 to -128
    char -> 16 bit      / All unicode characters
    short -> 16 bit     / from +32,767 to -32,768
    int -> 32 bit
    long -> 64 bit

    * * Floating-point **
    float -> 32 bit     / from 3.402,823,5 E+38 to 1.4 E-45
    double -> 64 bit

    * * Other **
    boolean             / false-true
    void
     */

    public static void main(String[] args) {
        // byte
        byte myByte = 127; // max
        byte myByte2 = -128; // min
        // short
        short myShort = 25000;
        // int
        int myInt = 1500000000;
        // long
        long myLong = 1000000000000000000L;
        // float
        float myFloat = 56.44f;
        // double
        double myDouble = 56.55;
        // char
        char myChar = 'B';
        // boolean
        boolean myBoolean = true;



    }

}
