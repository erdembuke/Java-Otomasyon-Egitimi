package Section02;

public class _02_IfElse {
    public static void main(String[] args) {
        int saat = 1000;

        if (saat < 1200)
            System.out.println("Saat ogleden once");


        saat = 1400;
        if (saat < 1200)
            System.out.println("saat ogleden once");
        else if (saat == 1200)
            System.out.println("saat oglen 12");
        else
            System.out.println("saat ogleden sonra");






    }
}
